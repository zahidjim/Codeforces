using namespace std;
ll solve()
{
    ll n,a,b;
    cin >> n >> a >> b;

    if(n<3)
    {
        return min(n*a,b);
    }
    else
    {
        if(b<3*a)
        {
            return (n/3)*b + min(b,(n%3)*a);
        }
        else
        {
            return n*a;
        }
    }
}
int main()
{
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);

    int t=1;
    cin >> t;
    while(t--)
    {
        cout << solve() << endl;
    }
    return 0;
}
